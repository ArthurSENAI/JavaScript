/*
TIPOS
    Adição(+)
    Subtração(-)
    Divisão(/)
    Multiplicação(*)
    Resto da Divisão(%)
    Incremento(++)
    Descremento(--)
    Expoente(**)
*/

// Adição
let v1 = 10;
let v2 = 15;
let soma = v1+v2;
console.log(v1+v2)
console.log("A soma é: "+soma)
console.log("A soma é:",v1+v2)
console.log("A soma é: "+parseInt(v1+v2))
console.log("A soma é: "+Number(v1+v2))

// Subtração
let v3 = 10;
let v4 = 5;
let subtracao = v3-v4;
console.log(v3-v4)
console.log("A subtração é: "+subtracao)
console.log("A subtração é:",v3-v4)
console.log("A subtração é: "+parseInt(v3-v4))
console.log("A subtração é: "+Number(v3-v4))

// Divisao
let v5 = 10;
